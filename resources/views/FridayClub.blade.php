<h1>Friday Club</h1>
<h2>Who?</h2>
<p>Friday club is for young people attending secondary school. We meet every week during term time in the church hall, enter via the side entrace in Balliol Road. If you're interested in coming, or bringing your children please do pop in on a Friday evening between 19:30 and 21:00 and speak to one of the leaders. Children are registered and there is a 50p charge each week to attend Friday club.</p>
<h2>What?</h2>
<h3>Games</h3>
<p>We play typical sports games such as football, hockey and basketball. As well as more specialist games, for example obstacle courses and nerf target games.</p>
<h3>Other activities</h3>
<p>We also have different activities every so often such as debates, creating radio programmes and first aid training. We also have club outings which have included bowling, ice skating and Jump Giants.</p>
<h3>Bible discussion</h3>
<p>Each week a different subject, bible passage or influential person will be presented to the young people with the option for them to ask questions and have a discussion. As a team we aim to choose subjects that will directly apply to their daily lives and will help them in some way.</p>
<h2>Why?</h2>
<p>We run our Friday club to give young people from the area the opportunity and environment to have fun and socialise outside of school. The bible discussion time gives them the chance to open up and receive to support from young adults on the team who have been through similar challenges, for example, GCSES.</p>
<p>Most of the leaders have attended Wednesday club, or similar groups as children and enjoyed them so much that they want to make sure others have the same opportuinites that they did.</p>


