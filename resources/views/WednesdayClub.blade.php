<h1>Wednesday Club</h1>
<h2>Who?</h2>
<p>Wednesday club is for children ages 6-11. We meet every week during term time in the church hall, enter via the side entrance in Balliol Road. If you're interested in bringing your children please do pop in on a Wednesday evening and speak to one of the leaders. Children are registered and there is a 50p charge each week to attend Wednesday club.</p>
<h2>What?</h2>
<h3>Games</h3>
<p>We offer a wide variety of games and activities for the children. We play typical sports games, such as football, hockey and basketball etc. We also have equipment for more specialist games; for example parachute games and obstacle courses.</p>
<h3>Craft</h3>
<p>We balance the active games with quieter craft activities. These activities range from baking different food to creating individual and group projects; such as bible story displays for the church and occasion based crafts like Christmas decorations and Mother’s day flowers.</p>
<h3>Story</h3>
<p>Each week there is a bible story given by one of the leaders. They are presented using drama, visual aids and games. The children have a quiz each week about the previous weeks’ story, where they can win sweets.</p>
<h3>Special events</h3>
<p>Each term we have a family fun night or Messy church event, where mums, dads, brothers, sisters etc. can join the usual club children for a special evening of games and activities. It is a great opportunity for the whole family to share in the fun we have on Wednesday evenings.</p>
<h2>Why? </h2>
<p>We run our Wednesday club to provide an opportunity for young people from the area to have fun, socialise and to learn about the bible in a comfortable and supportive environment. It gives the children the chance to make friends outside of school and play games they may never have had the chance or space to play before.</p>
<p>Most of the leaders have attended Wednesday club, or similar groups as children and enjoyed them so much that they want to make sure others have the same opportuinites that they did.</p>


